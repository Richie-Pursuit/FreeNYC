export function applySiteThemeToDocument(themeInput = DEFAULT_SITE_THEME_KEY) {
  if (typeof document === "undefined") {
    return;
  }

  const theme =
    typeof themeInput === "string"
      ? getSiteTheme(themeInput)
      : getSiteTheme(themeInput?.themeKey);
  const variables = getSiteThemeCssVariables(theme.key);
  const root = document.documentElement;

  Object.entries(variables).forEach(([key, value]) => {
    root.style.setProperty(key, value);
  });

  root.dataset.siteTheme = theme.key;
}
